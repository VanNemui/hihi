package baitap.QuanLiSinhVien;

public class Ngay {
	private int ngayy;
	private int thang;
	private int nam;
	
	public Ngay(int ngayy, int thang, int nam) {
		this.ngayy = ngayy;
		this.thang = thang;
		this.nam = nam;
	}

	public int getNgayy() {
		return ngayy;
	}

	public void setNgayy(int ngayy) {
		this.ngayy = ngayy;
	}

	public int getThang() {
		return thang;
	}

	public void setThang(int thang) {
		this.thang = thang;
	}

	public int getNam() {
		return nam;
	}

	public void setNam(int nam) {
		this.nam = nam;
	}

}
